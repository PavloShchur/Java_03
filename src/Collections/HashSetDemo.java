package Collections;

import java.util.HashSet;

public class HashSetDemo {

	public static void main(String[] args) {
		HashSet<String> hashSet = new HashSet<String>();
		
		hashSet.add("Бетта");
		hashSet.add("Альфа");
		hashSet.add("Йота");
		hashSet.add("Гама");
		hashSet.add("Іпсилон");
		hashSet.add("Омега");
		
		System.out.println(hashSet);

	}

}
