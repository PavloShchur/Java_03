package StringsClass;

public class valueOfMethod {

	public static void main(String[] args) {
		double d = 12939939.939;
		boolean b = true;
		long l = 1232874;
		
		char[] arr = {'a', 'b', 'c', 'd','e', 'f', 'g'};
		
		System.out.println("Returned value : " + String.valueOf(d));
		System.out.println("Returned value : " + String.valueOf(b));
		System.out.println("Returned value : " + String.valueOf(l));
		System.out.println("Returned value : " + String.valueOf(arr));

	}

}
